// ? parseInt and parseFloat are two JavaScript functions used to convert strings into numbers. 
let firstname = 'Punit';
let surname = '9999';

console.log(typeof(firstname));
console.log(typeof(surname));
console.log(firstname);
console.log(surname);

let parint = parseInt(firstname);
let parfloat = parseFloat(surname);

console.log(typeof(parint));
console.log(typeof(parfloat));
console.log(parint);
console.log(parfloat);